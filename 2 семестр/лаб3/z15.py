'''15. Дано ціле число N (> 0). Послідовність дійсних чисел A(K) визначається наступним чином:
A(0) = 1, A(K) = (A(K-1) + 1) / K, K = 1, 2, ....Вивести елементи A(1), A(2), ..., A(N)'''

print('enter N >0')
n=int(input())
a=1
k=0
if n>0:
 while k<n:
    k+=1
    a=(a+1)/k
    print('A(',k,')=',a)
else:
    print('error - N<0')